#include<stdio.h>

int boj(void)
{
	int i = 1;
	return (int)(*(char*)&i);
}

int main()
{
	int i = boj();
	if (i == 1)
	{
		printf("小端\n");
	}
	else if (i == 0)
	{
		printf("大端\n");
	}
	else
	{
		printf("error!\n");
	}
	return 0;
}